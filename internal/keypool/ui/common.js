/* ========== Helpers ========== */

function formatTokens(n) {
    if (n == null || isNaN(n)) return '-';
    n = Number(n);
    if (n >= 1e9) return (n / 1e9).toFixed(1).replace(/\.0$/, '') + 'B';
    if (n >= 1e6) return (n / 1e6).toFixed(1).replace(/\.0$/, '') + 'M';
    if (n >= 1e3) return (n / 1e3).toFixed(1).replace(/\.0$/, '') + 'K';
    return n.toLocaleString();
}

function maskKey(key) {
    if (key.length <= 8) return '••••' + key.slice(-4);
    return key.slice(0, 4) + '••••' + key.slice(-4);
}

function formatDate(dateStr) {
    return new Date(dateStr).toLocaleDateString('zh-CN', { year: 'numeric', month: '2-digit', day: '2-digit' });
}

function showToast(msg, isError) {
    const t = document.getElementById('toast');
    t.textContent = msg;
    t.className = 'toast show' + (isError ? ' error' : '');
    clearTimeout(t._timer);
    t._timer = setTimeout(() => { t.className = 'toast'; }, 2500);
}

function formatJSON(str) {
    const esc = s => s.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;');
    try { return esc(JSON.stringify(JSON.parse(str), null, 2)); } catch(e) {}
    if (str.startsWith('data: ')) {
        return str.split('\n').map(line => {
            if (line.startsWith('data: ')) {
                const payload = line.slice(6);
                if (payload === '[DONE]') return '<span style="color:hsl(var(--muted-foreground))">data: [DONE]</span>';
                try { return 'data: ' + esc(JSON.stringify(JSON.parse(payload), null, 2)); } catch(e) {}
            }
            return esc(line);
        }).join('\n');
    }
    return esc(str);
}

